#ifndef CHECKERS_HPP
#define CHECKERS_HPP

#include <vector>
#include <utility>

/**
 * @brief Перечисление, представляющее тип содержимого клетки игровой доски.
 */
enum class Piece {
    Empty,     ///< Пустая клетка
    White,     ///< Белая шашка
    Black,     ///< Черная шашка
    WhiteKing, ///< Белая дама
    BlackKing  ///< Черная дама
};

/**
 * @brief Класс, реализующий полную бизнес-логику игры в шашки на доске 8x8.
 * @details Класс управляет состоянием доски, проверяет корректность ходов 
 * по правилам, обрабатывает обязательные взятия и идущие подряд срубы.
 */
class CheckersBoard {
private:
    /**
     * @brief Двумерный динамический массив, хранящий состояние игрового поля 8х8.
     */
    std::vector<std::vector<Piece>> board;

    /**
     * @brief Флаг очередности хода.
     * @details Равен true, если сейчас ходят белые, и false, если ходят черные.
     */
    bool whiteTurn;

    /**
     * @brief Флаг режима мульти-боя.
     * @details Становится true, если фигура совершила сруб и обязана рубить дальше.
     */
    bool isMultiCaptureMode = false;

    /**
     * @brief Координаты текущей активной фигуры, выполняющей серию срубов.
     * @details Используется в режиме мульти-боя, хранит пару индексов {строка, столбец}.
     */
    std::pair<int, int> activePieceCurrentPos = {-1, -1};

    /**
     * @brief Внутренний метод для проверки возможности совершить взятие конкретной фигурой.
     * @param row Индекс строки проверяемой клетки (0-7).
     * @param col Индекс столбца проверяемой клетки (0-7).
     * @return true, если фигура из этой клетки может срубить хотя бы одну вражескую шашку, иначе false.
     */
    bool canPieceCapture(int row, int col) const;

public:
    /**
     * @brief Конструктор по умолчанию.
     * @details Выделяет память под поле 8х8, очищает его и расставляет по 12 начальных 
     * шашек для каждого игрока на черных клетках. Устанавливает начальный ход белых.
     */
    CheckersBoard();

    /**
     * @brief Возвращает тип фигуры, находящейся в указанных координатах игрового поля.
     * @param row Индекс строки поля (0-7).
     * @param col Индекс столбца поля (0-7).
     * @return Значение перечисления Piece, описывающее содержимое клетки.
     * @throws std::out_of_range Если переданные координаты выходят за допустимые границы доски (0-7).
     */
    Piece getPiece(int row, int col) const;

    /**
     * @brief Возвращает информацию о том, чей сейчас ход.
     * @return true, если сейчас очередь хода белых, иначе false.
     */
    bool isWhiteTurn() const;

    /**
     * @brief Выводит текущее графическое состояние доски и счетчик съеденных фигур в консоль.
     */
    void displayBoard() const;

    /**
     * @brief Выполняет перемещение или взятие фигуры по заданным координатам с полной валидацией.
     * @param fromRow Начальная строка фигуры (0-7).
     * @param fromCol Начальный столбец фигуры (0-7).
     * @param toRow Целевая строка для перемещения (0-7).
     * @param toCol Целевой столбец для перемещения (0-7).
     * @throws std::invalid_argument Если ход нарушает правила игры: ход не по диагонали, 
     * попытка проигнорировать обязательный бой, ход чужой фигурой, неверная дистанция и т.д.
     * @throws std::out_of_range Если координаты начала или конца выходят за пределы матрицы.
     */
    void makeMove(int fromRow, int fromCol, int toRow, int toCol);

    /**
     * @brief Проверяет, остались ли на игровом поле фигуры запрашиваемого цвета.
     * @param checkWhite Если true - проверяется наличие белых фигур, если false - черных.
     * @return true, если у запрашиваемой стороны осталась хотя бы одна фигура на поле, иначе false.
     */
    bool hasPieces(bool checkWhite) const;

    /**
     * @brief Возвращает количество съеденных фигур противника.
     * @param ofWhite Если true - рассчитывает сколько белых фигур потеряно, если false - сколько черных.
     * @return Целое число от 0 до 12 - количество уничтоженных с поля фигур.
     */
    int getCapturedCount(bool ofWhite) const;

    /**
     * @brief Проверяет наличие обязательных ходов со взятием на всем поле для текущего игрока.
     * @details Метод сканирует доску на наличие ситуаций, в которых игрок обязан срубить врага.
     * @return true, если на поле есть доступный бой и игрок обязан ходить им, иначе false.
     */
    bool hasObligatoryCaptures() const;
};

#endif // CHECKERS_HPP